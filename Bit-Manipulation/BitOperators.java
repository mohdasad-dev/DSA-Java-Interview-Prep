import java.util.*;
public class BitOperators{
    public static void main(String arg[]){
        // Binary AND
        System.out.println((5 & 6));
        //  Binary OR
        System.out.println((5 | 6));
        // Binary XOR
        System.out.println((5 ^ 6));
        //  Binary One's Complement
        System.out.println((~5));
        //  Binary One's Complement
        System.out.println((~0));
        //  Binary Left Shift
        System.out.println((5<<2));
        // Binary Right Shift
        System.out.println((6>>1));
    }
}